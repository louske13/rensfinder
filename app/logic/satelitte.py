# Vérification des doublons (liens archivés)
def is_duplicate(link):
    if not os.path.exists(archive_file):
        return False
    with open(archive_file, 'r') as f:
        stored_links = f.read().splitlines()
    # Vérification stricte des doublons par hachage
    hashed_link = hashlib.md5(link.encode('utf-8')).hexdigest()
    return any(hashed_link in line for line in stored_links)

def mark_as_sent(link, lang):
    hashed_link = hashlib.md5(link.encode('utf-8')).hexdigest()
    with open(archive_file, 'a') as f:
        f.write(f"{datetime.now().isoformat()} | {lang} | {hashed_link}\n")

