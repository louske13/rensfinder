def analyze_feed(rss_feed, lang):
    logging.info(f"Analyse du flux : {rss_feed}")
    
    # Analyse du flux RSS
    feed = feedparser.parse(rss_feed)
    
    if not feed.entries:
        logging.warning(f"Flux vide ou invalide : {rss_feed}")
        return []  # Si aucun article, retour vide

    found_articles = []
    seen_titles = set()  # Pour éviter les doublons d'articles
    max_date_threshold = datetime.now() - timedelta(days=MAX_ARTICLE_AGE_DAYS)

    for entry in feed.entries:
        # Vérifier que les champs essentiels existent
        if not hasattr(entry, 'title') or not hasattr(entry, 'link'):
            logging.warning(f"Article sans titre ou lien ignoré : {entry}")
            continue
        
        if entry.title in seen_titles:  # Éviter les doublons
            continue
        seen_titles.add(entry.title)

        if is_duplicate(entry.link):  # Vérifie si l'article a déjà été envoyé
            continue

        # Vérifie la date de l'article
        try:
            if hasattr(entry, 'published_parsed') and entry.published_parsed:
                article_date = datetime(*entry.published_parsed[:6])
                if article_date < max_date_threshold:
                    continue
            else:
                # Ajouter une date par défaut si aucune date n'est trouvée
                article_date = datetime.now()
                logging.warning(f"Aucune date trouvée pour l'article : {entry.title}. Date par défaut attribuée.")
        except Exception as e:
            logging.warning(f"Erreur de traitement de la date : {e}")
            continue

        # Analyse des mots-clés dans le titre, le résumé et le contenu complet
        content = ''
        if hasattr(entry, 'content') and entry.content:
            content = ' '.join([c.get('value', '') for c in entry.content if isinstance(c, dict)])
        elif hasattr(entry, 'summary'):
            content = entry.summary  # Utiliser le résumé si le contenu n'est pas disponible

        # Recherche des mots-clés
        keywords_found = [
            kw for kw in keywords_by_language.get(lang, [])
            if (kw.lower() in entry.title.lower() or  # Cherche dans le titre
                kw.lower() in content.lower())  # Cherche dans le contenu ou le résumé
        ]

        if keywords_found:  # Si des mots-clés sont trouvés
            try:
                translated_title = translator.translate(entry.title, dest='fr').text
                found_articles.append({
                    "title": entry.title,
                    "translated_title": translated_title,
                    "link": entry.link,
                    "flag": flags_by_language.get(lang, "🌍"),
                    "keywords": keywords_found
                })
                mark_as_sent(entry.link, lang)  # Marquer l'article comme envoyé
            except Exception as e:
                logging.warning(f"Erreur lors de la traduction ou de l'ajout de l'article : {e}")

    return found_articles
