import time
import feedparser
from datetime import datetime, timedelta

FLUX_INUTILES_FILE = "flux_inutiles.txt"  # Fichier où seront enregistrés les flux inutiles

def enregistrer_flux_inutiles(feed_url):
    """Enregistre les flux RSS qui ne fonctionnent jamais dans un fichier."""
    with open(FLUX_INUTILES_FILE, "a", encoding="utf-8") as f:
        f.write(feed_url + "\n")
    logging.warning(f"⚠️ Flux inutile détecté et enregistré : {feed_url}")

def analyze_feed(feed_url, sent_articles, first_filter_keywords, categories, days_limit=1):
    """Analyse un flux RSS et filtre les articles, en enregistrant les flux inutiles."""
    logging.info(f"📡 Analyse du flux RSS : {feed_url}")

    # 🔹 Vérifier si ce flux est déjà dans la liste des flux inutiles
    if os.path.exists(FLUX_INUTILES_FILE):
        with open(FLUX_INUTILES_FILE, "r", encoding="utf-8") as f:
            flux_inutiles = set(f.read().splitlines())
        if feed_url in flux_inutiles:
            logging.warning(f"🚫 Ignoré (déjà marqué comme inutile) : {feed_url}")
            return {category: [] for category in categories}  # Retourne un résultat vide

    # 🔹 Tentative de récupération du flux avec gestion des erreurs
    max_attempts = 3  # Nombre de tentatives avant d'abandonner
    for attempt in range(max_attempts):
        try:
            feed = feedparser.parse(feed_url)

            # Vérifier si le flux est valide
            if not feed.entries:
                logging.warning(f"⚠️ Aucun article trouvé dans {feed_url}. Tentative {attempt+1}/{max_attempts}")
                time.sleep(2)  # Attente avant de réessayer
                continue

            # 🔹 Si le flux est valide, on continue l'analyse
            cutoff_date = datetime.now() - timedelta(days=days_limit)
            categorized = {category: [] for category in categories}

            for entry in feed.entries:
                if hasattr(entry, "title") and hasattr(entry, "link"):
                    # Vérifier si l'article a une date, sinon utiliser la date actuelle
                    if hasattr(entry, "published_parsed") and entry.published_parsed:
                        published_date = datetime(*entry.published_parsed[:6])
                    else:
                        published_date = datetime.now()

                    # Ignorer les articles trop anciens ou déjà envoyés
                    if published_date < cutoff_date or entry.link in sent_articles:
                        continue

                    # Premier filtre : vérifier si le titre contient un mot-clé de région
                    if any(keyword.lower() in entry.title.lower() for keyword in first_filter_keywords):
                        # Deuxième filtre : classer dans les catégories
                        for category, keywords in categories.items():
                            if any(keyword.lower() in entry.title.lower() for keyword in keywords):
                                categorized[category].append({
                                    "title": entry.title,
                                    "link": entry.link,
                                    "date": published_date.strftime("%Y-%m-%d %H:%M:%S")
                                })
            return categorized

        except Exception as e:
            logging.error(f"🚨 Erreur lors du traitement de {feed_url} : {e}")
            time.sleep(2)  # Pause avant de réessayer
    
    # 🔹 Si après 3 tentatives le flux ne fonctionne pas, on l'enregistre comme inutilisable
    logging.error(f"❌ Impossible de récupérer {feed_url} après {max_attempts} tentatives. Il sera ignoré.")
    enregistrer_flux_inutiles(feed_url)
    return {category: [] for category in categories}  # Retourne des catégories vides si erreur

